            <h2>Sobre o AtOn</h2>


            <p>AtOn é um serviço de descoberta desenvolvido no Grupo de Pesquisa: “Laboratório de Estudos Métricos da Informação na Web (Lab-iMetrics/UFAL)” contemplado no Edital Chamada Universal MCTI/CNPq Nº 01/2016, pelo processo nº 426777/2016-6, no âmbito do Projeto: “Monitoramento de Métricas Alternativas e Atenção Online de Artigos de Periódicos da Ciência da Informação”.</p>
            <p>A solução foi desenvolvida utilizando tecnologias: PHP, Elasticsearch e APIs do Facebook e CrossRef.</p>
            <p>Tendo o foco especial na Atenção Online, o AtOn se diferencia de outras bases referecniais da área por possíbiliar a identificação da visibilidade que artigos de periócicos de Ciência da Informação alcançam após a publicação e ordenar os resultados de busca a partir desse indicador.</p>
            <p>O projeto é coordenado pelo Prof. Ronaldo Ferreira de Araújo do Curso de Biblioteconomia do Instituto de Ciências Humanas, Comunicação e Artes da Universidade Federal de Alagoas (CB/ICHCA/UFAL) com colaboração do Tiago Rodrigo Marçal Murakami do Sistema Integrado de Bibliotecas da Universidade de São Paulo (USP) a participação de bolsistas e colaboradores de iniciação científica e de desenvolvimento tecnológico.</p>
